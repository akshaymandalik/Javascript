// Swapping 2 Numbers without 3rd Variable

let iVal1 = 10;
let iVal2 = 11;

console.log("Values Before Swapping:");
console.log("Value1:" + iVal1);
console.log("Value2:"+iVal2);


iVal1 = iVal1 + iVal2;
iVal2 = iVal1 - iVal2;
iVal1 = iVal1 - iVal2;

console.log("\nValues After Swapping:");
console.log("Value1:" + iVal1);
console.log("Value2:"+iVal2);


